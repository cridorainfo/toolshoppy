#!/usr/bin/env node
/**
 * ToolShoppy — sync Google Trends CSV → SEO assets
 *
 * Usage:
 *   npm run sync:trends
 *   npm run sync:trends -- --csv "C:/path/to/raising google trends.csv"
 *
 * Updates:
 *   - assets/data/google-trends.csv (copy if external path given)
 *   - assets/js/trends-data.js
 *   - assets/js/trends-seo.js (regenerated runtime)
 *   - SEO landing pages from template
 *   - sitemap.xml (trend landing URLs)
 *   - _redirects (pretty URLs)
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import {
  TOOL_PATHS,
  NOISE_PATTERNS,
  MAP_RULES,
  LANDING_PAGES,
} from './trends-config.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');
const DEFAULT_CSV = path.join(ROOT, 'assets/data/google-trends.csv');
const DESKTOP_CSV = path.join(process.env.USERPROFILE || process.env.HOME || '', 'Desktop/raising google trends.csv');
const TEMPLATE = path.join(ROOT, 'assets/templates/seo-trend-landing.html');

function parseArgs() {
  const args = process.argv.slice(2);
  let csv = DEFAULT_CSV;
  for (let i = 0; i < args.length; i++) {
    if (args[i] === '--csv' && args[i + 1]) csv = path.resolve(args[++i]);
  }
  return { csv };
}

function parseCsv(text) {
  const rows = [];
  for (const line of text.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed) continue;

    let query = '';
    let interest = 0;
    let growth = '';

    if (trimmed.startsWith('"')) {
      const end = trimmed.indexOf('"', 1);
      query = trimmed.slice(1, end);
      const rest = trimmed.slice(end + 2).split(',');
      interest = parseInt(rest[0], 10) || 0;
      growth = rest.slice(1).join(',').trim();
    } else {
      const parts = trimmed.split(',');
      query = parts[0];
      if (query === 'query') continue;
      interest = parseInt(parts[1], 10) || 0;
      growth = parts.slice(2).join(',').trim();
    }

    if (!query || query === 'query') continue;
    rows.push({ query, interest, growth });
  }
  return rows;
}

function normalize(s) {
  return (s || '').toLowerCase().replace(/[^a-z0-9\s]/g, ' ').replace(/\s+/g, ' ').trim();
}

function parseGrowth(g) {
  if (!g || g === 'Breakout') return g === 'Breakout' ? 999 : 0;
  const n = parseInt(String(g).replace(/[%+]/g, ''), 10);
  return Number.isNaN(n) ? 0 : n;
}

function isNoise(query) {
  const n = normalize(query);
  return NOISE_PATTERNS.some((re) => re.test(n));
}

function mapQuery(query) {
  const n = normalize(query);
  if (!n || isNoise(n)) return null;
  for (const rule of MAP_RULES) {
    if (rule.test.test(n)) return rule.tool;
  }
  return null;
}

function resolveCsvPath(requested) {
  if (fs.existsSync(requested)) return requested;
  if (requested === DEFAULT_CSV && fs.existsSync(DESKTOP_CSV)) {
    console.log('Using Desktop CSV:', DESKTOP_CSV);
    return DESKTOP_CSV;
  }
  throw new Error('CSV not found: ' + requested);
}

function buildQueries(rows) {
  const byKey = new Map();
  for (const row of rows) {
    const tool = mapQuery(row.query);
    if (!tool) continue;
    const key = normalize(row.query);
    const growth = parseGrowth(row.growth);
    const score = row.interest + Math.min(growth, 200) * 0.1;
    const existing = byKey.get(key);
    if (!existing || row.interest > existing.interest) {
      byKey.set(key, {
        q: row.query,
        tool,
        interest: row.interest,
        growth,
        score,
      });
    }
  }
  return [...byKey.values()].sort((a, b) => b.interest - a.interest);
}

function keywordsForTool(queries, toolId, limit = 8) {
  return queries
    .filter((r) => r.tool === toolId)
    .slice(0, limit)
    .map((r) => r.q)
    .join(', ');
}

function writeTrendsData(queries, landing, generatedAt, source) {
  const landingObj = {};
  landing.forEach((l) => { landingObj[l.id] = l.path; });

  const content = `// AUTO-GENERATED by scripts/sync-trends-seo.mjs — do not edit manually
// Source: ${source}
// Generated: ${generatedAt}
(function (global) {
  'use strict';
  global.TSTrendsData = ${JSON.stringify({
    generatedAt,
    source: path.relative(ROOT, source).replace(/\\/g, '/'),
    queryCount: queries.length,
    queries: queries.map(({ q, tool, interest, growth }) => ({ q, tool, interest, growth })),
    landing: landingObj,
    redirects: landing.filter((l) => l.redirectSlug).map((l) => ({
      from: '/' + l.redirectSlug,
      to: l.path,
    })),
  }, null, 2)};
})(window);
`;
  fs.writeFileSync(path.join(ROOT, 'assets/js/trends-data.js'), content);
}

function writeTrendsSeoJs() {
  const content = `// ToolShoppy — Google Trends SEO resolver (runtime)
// Data: assets/js/trends-data.js (auto-generated by npm run sync:trends)
(function (global) {
  'use strict';

  var TOOL_PATHS = ${JSON.stringify(TOOL_PATHS, null, 2)};

  var QUERIES = (global.TSTrendsData && global.TSTrendsData.queries) || [];
  var LANDING = (global.TSTrendsData && global.TSTrendsData.landing) || {};

  var TOOL_SCORES = {};
  var QUERY_INDEX = [];

  function normalize(s) {
    return (s || '').toLowerCase().replace(/[^a-z0-9\\s]/g, ' ').replace(/\\s+/g, ' ').trim();
  }

  function buildIndex() {
    TOOL_SCORES = {};
    QUERY_INDEX = [];
    QUERIES.forEach(function (row) {
      var key = normalize(row.q);
      QUERY_INDEX.push({ key: key, tool: row.tool, interest: row.interest || 0, q: row.q });
      TOOL_SCORES[row.tool] = Math.max(TOOL_SCORES[row.tool] || 0, row.interest || 0);
    });
    QUERY_INDEX.sort(function (a, b) { return b.interest - a.interest; });
  }
  buildIndex();

  function hrefForTool(toolId) {
    return TOOL_PATHS[toolId] || '/';
  }

  function resolveQuery(raw) {
    var n = normalize(raw);
    if (!n) return null;
    var best = null;
    var bestScore = 0;
    QUERY_INDEX.forEach(function (row) {
      var score = 0;
      if (n === row.key) score = 1000 + row.interest;
      else if (n.indexOf(row.key) >= 0 || row.key.indexOf(n) >= 0) score = 500 + row.interest;
      else {
        var terms = n.split(' ');
        var hits = 0;
        terms.forEach(function (t) {
          if (t.length > 2 && row.key.indexOf(t) >= 0) hits++;
        });
        if (hits >= 2) score = 200 + row.interest + hits * 10;
        else if (hits === 1 && terms.length <= 3) score = 80 + row.interest;
      }
      if (score > bestScore) { bestScore = score; best = row; }
    });
    if (!best || bestScore < 80) return null;
    return {
      toolId: best.tool,
      href: hrefForTool(best.tool),
      query: best.q,
      score: bestScore,
      redirect: bestScore >= 500,
    };
  }

  function getToolScore(toolId) {
    return TOOL_SCORES[toolId] || 0;
  }

  function getTrendingTools(limit) {
    limit = limit || 12;
    return Object.keys(TOOL_SCORES).map(function (id) {
      var top = QUERY_INDEX.find(function (r) { return r.tool === id; });
      return { toolId: id, score: TOOL_SCORES[id], topQuery: top ? top.q : '', href: hrefForTool(id) };
    }).sort(function (a, b) { return b.score - a.score; }).slice(0, limit);
  }

  function getTrendingQueries(limit) {
    limit = limit || 14;
    return QUERY_INDEX.slice(0, limit).map(function (r) {
      return { q: r.q, tool: r.tool, href: hrefForTool(r.tool), interest: r.interest };
    });
  }

  global.TSTrendsSEO = {
    TOOL_PATHS: TOOL_PATHS,
    LANDING: LANDING,
    resolveQuery: resolveQuery,
    getToolScore: getToolScore,
    getTrendingTools: getTrendingTools,
    getTrendingQueries: getTrendingQueries,
    normalize: normalize,
    reload: buildIndex,
  };

  if (global.TSTrendsData) buildIndex();
})(window);
`;
  fs.writeFileSync(path.join(ROOT, 'assets/js/trends-seo.js'), content);
}

function writeLandingPages(queries) {
  const tpl = fs.readFileSync(TEMPLATE, 'utf8');
  for (const page of LANDING_PAGES) {
    const toolHref = TOOL_PATHS[page.tool] || '/';
    const keywords = keywordsForTool(queries, page.tool) || page.h1.toLowerCase();
    const html = tpl
      .replace(/__TITLE__/g, page.title)
      .replace(/__DESC__/g, page.desc)
      .replace(/__KEYWORDS__/g, keywords)
      .replace(/__PATH__/g, page.path)
      .replace(/__H1__/g, page.h1)
      .replace(/__SUB__/g, page.sub)
      .replace(/__BODY__/g, page.body)
      .replace(/__TOOL__/g, toolHref)
      .replace(/__ICON__/g, page.icon);
    const dir = path.join(ROOT, page.path.replace(/^\//, '').replace(/\/$/, ''));
    fs.mkdirSync(dir, { recursive: true });
    fs.writeFileSync(path.join(dir, 'index.html'), html);
    console.log('  landing', page.path);
  }
}

function updateSitemap(landingPages, date) {
  const sitemapPath = path.join(ROOT, 'sitemap.xml');
  let xml = fs.readFileSync(sitemapPath, 'utf8');
  const base = 'https://toolshoppy.com';

  for (const page of landingPages) {
    const loc = base + page.path;
    if (xml.includes(loc)) continue;
    const entry = `  <url><loc>${loc}</loc><lastmod>${date}</lastmod><changefreq>weekly</changefreq><priority>${page.priority || 0.85}</priority></url>\n`;
    xml = xml.replace('</urlset>', entry + '</urlset>');
    console.log('  sitemap +', page.path);
  }

  fs.writeFileSync(sitemapPath, xml);
}

function updateRedirects(landingPages) {
  const redirectsPath = path.join(ROOT, '_redirects');
  let text = fs.readFileSync(redirectsPath, 'utf8');
  const start = '# TRENDS-AUTO-START';
  const end = '# TRENDS-AUTO-END';

  const lines = ['/search /search/ 301'];
  for (const page of landingPages) {
    if (page.redirectSlug) {
      lines.push(`/${page.redirectSlug} ${page.path} 301`);
    }
  }
  const block = [start, ...lines, end].join('\n');

  if (text.includes(start) && text.includes(end)) {
    text = text.replace(new RegExp(start + '[\\s\\S]*?' + end), block);
  } else {
    text = text.trimEnd() + '\n\n' + block + '\n';
  }
  fs.writeFileSync(redirectsPath, text);
  console.log('  redirects:', lines.length, 'rules');
}

function updateIndexKeywords(queries) {
  const indexPath = path.join(ROOT, 'index.html');
  let html = fs.readFileSync(indexPath, 'utf8');
  const top = queries.slice(0, 10).map((q) => q.q).join(', ');
  const meta = `<meta name="keywords" content="${top}, toolshoppy, free online tools">`;
  html = html.replace(/<meta name="keywords" content="[^"]*">/, meta);
  fs.writeFileSync(indexPath, html);
}

function main() {
  const { csv: csvArg } = parseArgs();
  const csvPath = resolveCsvPath(csvArg);
  const generatedAt = new Date().toISOString().slice(0, 10);

  console.log('Reading', csvPath);
  const rows = parseCsv(fs.readFileSync(csvPath, 'utf8'));
  console.log('Parsed', rows.length, 'CSV rows');

  if (csvPath !== DEFAULT_CSV) {
    fs.mkdirSync(path.dirname(DEFAULT_CSV), { recursive: true });
    fs.copyFileSync(csvPath, DEFAULT_CSV);
    console.log('Copied CSV → assets/data/google-trends.csv');
  }

  const queries = buildQueries(rows);
  console.log('Mapped', queries.length, 'queries to tools');

  writeTrendsData(queries, LANDING_PAGES, generatedAt, csvPath);
  console.log('Wrote assets/js/trends-data.js');

  writeTrendsSeoJs();
  console.log('Wrote assets/js/trends-seo.js');

  console.log('Generating landing pages…');
  writeLandingPages(queries);

  updateSitemap(LANDING_PAGES, generatedAt);
  updateRedirects(LANDING_PAGES);
  updateIndexKeywords(queries);

  const topTools = [...new Set(queries.slice(0, 15).map((q) => q.tool))];
  console.log('\nDone. Top mapped tools:', topTools.join(', '));
  console.log('Deploy, then submit sitemap in Google Search Console.');
}

main();
